CREATE DATABASE PARTE1
GO
USE PARTE1
GO
CREATE TABLE CLIENTES(
  DNI VARCHAR(10) NOT NULL PRIMARY KEY,
  APELLIDOS VARCHAR(50) NOT NULL,
  NOMBRES VARCHAR(50) NOT NULL
)
GO
CREATE TABLE ACCIONES(
  CODIGO INT NOT NULL PRIMARY KEY,
  NOMBRE VARCHAR(50) NOT NULL
)
GO
CREATE TABLE COTIZACIONES(
  CODIGO INT NOT NULL FOREIGN KEY REFERENCES ACCIONES(CODIGO),
  FECHA DATE NOT NULL,
  VALOR MONEY NOT NULL,
  PRIMARY KEY (CODIGO, FECHA)
)
GO
CREATE TABLE CARTERA( --ACCIONES X CLIENTE
  TRANSACCION BIGINT NOT NULL PRIMARY KEY IDENTITY(1,1),
  ACCION INT NOT NULL FOREIGN KEY REFERENCES ACCIONES(CODIGO),
  DNI VARCHAR(10) NOT NULL FOREIGN KEY REFERENCES CLIENTES(DNI),
  FECHA DATE NOT NULL,
  CANTIDAD INT NOT NULL --OPCIONAL: NO LO INDICABA EL ENUNCIADO
)