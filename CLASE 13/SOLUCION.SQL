CREATE TRIGGER TR_ELIMINAR_CUENTAS ON CUENTAS
INSTEAD OF DELETE
AS
BEGIN
DECLARE @IDCUENTA BIGINT
SELECT @IDCUENTA = IDCUENTA FROM DELETED
DELETE FROM TARJETAS WHERE IDCUENTA = @IDCUENTA
DELETE FROM CUENTAS WHERE IDCUENTA = @IDCUENTA
END

