CREATE TRIGGER TR_MODIFICAR_ESTADO ON CLIENTES
INSTEAD OF DELETE
AS
BEGIN
	UPDATE CLIENTES SET ESTADO = 0 
	WHERE IDCLIENTE = ( SELECT IDCLIENTE FROM DELETED)
END


