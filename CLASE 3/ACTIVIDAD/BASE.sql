CREATE DATABASE ALMACEN
GO
USE ALMACEN
GO
CREATE TABLE PAISES(
	IDPAIS INT NOT NULL PRIMARY KEY,
	NOMBRE VARCHAR(50) NOT NULL
)
GO
CREATE TABLE PROVINCIAS(
	IDPROVINCIA INT NOT NULL PRIMARY KEY,
	NOMBRE VARCHAR(50),
	IDPAIS INT NOT NULL FOREIGN KEY REFERENCES PAISES(IDPAIS)
)
GO
CREATE TABLE LOCALIDADES(
	IDLOCALIDAD INT NOT NULL PRIMARY KEY,
	NOMBRE VARCHAR (50) NOT NULL,
	IDPROVINCIA INT NOT NULL FOREIGN KEY REFERENCES PROVINCIAS(IDPROVINCIA)
) 
GO
CREATE TABLE CLIENTES(
	DNI INT NOT NULL PRIMARY KEY,
	NOMBRE VARCHAR (50)NOT NULL,
	APELLIDO VARCHAR (50)NOT NULL,
	FNAC DATETIME NULL,
	CALLE VARCHAR(50) NULL,
	SEXO CHAR NOT NULL,
	IDLOCALIDAD INT NOT NULL FOREIGN KEY REFERENCES LOCALIDADES(IDLOCALIDAD)
)
GO
CREATE TABLE VENTAS(
	IDVENTA INT NOT NULL PRIMARY KEY,
	FECHA DATETIME NOT NULL,
	DNI INT NOT NULL FOREIGN KEY REFERENCES CLIENTES(DNI)
)
GO
CREATE TABLE MARCAS(
	IDMARCA INT NOT NULL PRIMARY KEY,
	DESCRIPCION VARCHAR(50)NOT NULL 
)
GO
CREATE TABLE ARTICULOS(
	IDARTICULO INT NOT NULL PRIMARY KEY,
	DESCRIPCION VARCHAR(50)NOT NULL,
	IDMARCA INT NULL FOREIGN KEY REFERENCES MARCAS(IDMARCA),
	ORIGEN CHAR NOT NULL,
	STOCK INT NOT NULL,
	PU FLOAT NOT NULL
) 
GO
CREATE TABLE ARTICULOS_X_VENTA(
	IDVENTA INT NOT NULL,
	IDARTICULO INT NOT NULL,
	CANTIDAD INT NOT NULL,
	PU FLOAT NOT NULL
	PRIMARY KEY (IDVENTA,IDARTICULO),
	FOREIGN KEY(IDVENTA)REFERENCES VENTAS(IDVENTA),
	FOREIGN KEY(IDARTICULO)REFERENCES ARTICULOS(IDARTICULO)
)
GO 
CREATE TABLE CATEGORIAS(
	IDCATEGORIA INT NOT NULL PRIMARY KEY,
	CATEGORIA VARCHAR(30) NOT NULL
)
GO
CREATE TABLE CATEGORIAS_X_ARTICULO(
	IDARTICULO INT NOT NULL FOREIGN KEY REFERENCES ARTICULOS (IDARTICULO),
	IDCATEGORIA INT NOT NULL FOREIGN KEY REFERENCES CATEGORIAS (IDCATEGORIA),
	PRIMARY KEY (IDARTICULO, IDCATEGORIA)
)
GO
