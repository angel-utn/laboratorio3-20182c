  DNI INT NOT NULL FOREIGN KEY REFERENCES PERSONAS(DNI),
  FECHA DATE NOT NULL,
  IMPORTE MONEY NOT NULL,
  PLAZO INT NOT NULL,
  CANCELADO BIT NOT NULL
)
